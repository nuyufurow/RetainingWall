function [ K ] = weiyibianjie02( n,K )
%此子函数为挡墙铰结时的位移边界条件,并输出划行划列之后的总刚度矩阵
%输入的K为总刚度矩阵，n为挡墙层数
if n==1 
   K=K([3,4,6],[3,4,6]);% 采用划行划列，排除1节点2个位移；排除2节点y位移
elseif n==2 
   K=K([3,4,6,7,9],[3,4,6,7,9]);% 采用划行划列，排除1节点2个位移；排除2、3节点y位移
elseif n==3 
   K=K([3,4,6,7,9,10,12],[3,4,6,7,9,10,12]);% 采用划行划列，排除1节点2个位移；排除2、3、4节点y位移
 elseif n==4 
   K=K([3,4,6,7,9,10,12,13,15],[3,4,6,7,9,10,12,13,15]);% 采用划行划列，排除1节点2个位移；排除2、3、4、5节点y位移  

end

